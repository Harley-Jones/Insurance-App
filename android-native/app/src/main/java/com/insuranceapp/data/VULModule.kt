# Policy Loan Dynamics in VUL

## Types of VUL Loans

**1. Standard Loans**
- Fixed interest rate (typically 5-8%)
- Continues to be charged while loan outstanding
- Cash value continues to grow in subaccounts

**2. Fixed Loans**
- Borrowed amount moved to fixed account
- Lower loan interest rate
- Reduced growth potential on borrowed amount

**3. Indexed Loans** (rare in VUL)
