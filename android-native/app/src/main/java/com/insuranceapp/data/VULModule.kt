## B. COI Escalation (The Silent Killer)

**How COI Works:**
- Cost of insurance rises each year
- Based on:
  - Age (mortality risk increases)
  - Death benefit amount
  - Health class

**The Compounding Problem:**

Age 40: $50/month COI
Age 50: $100/month COI
Age 60: $250/month COI
Age 70: $600/month COI
Age 80: $1,500/month COI

**If cash value growth doesn't keep pace:**
- COI consumes cash value
- Policy enters death spiral
- Eventually collapses

**Why minimum-funded VUL is dangerous:**
Insufficient cash value to absorb rising COI in later years

## C. Lapse-and-Tax Event (The Nightmare Scenario)

**What Triggers This:**
1. Policy becomes underfunded
2. Owner stops paying premiums
3. Cash value insufficient to cover COI
4. Policy lapses with outstanding loans

**Tax Consequences:**
- IRS treats loan as distribution at lapse
- Entire gain becomes taxable
- Tax bill due immediately
- No cash value left to pay taxes from

