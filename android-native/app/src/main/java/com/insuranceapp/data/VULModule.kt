- Can gain significantly or lose value

## C. Death Benefit Options

**Option A: Level Death Benefit**
- Face amount remains constant
- Cash value growth doesn't increase death benefit
- More cost-efficient if focused on death benefit

**Option B: Increasing Death Benefit**
- Death benefit = Face amount + Cash value
- As cash value grows, death benefit increases
- Higher cost of insurance charges

## D. Fees and Charges (Critical)

VUL policies have **multiple fee layers:**

**Mortality & Expense (M&E) Charges:**
- Typically 0.5-1.5% annually
- Covers insurance company profit and risk

**Cost of Insurance (COI):**
- Monthly charge for death benefit
- Increases with age
- Based on mortality tables

**Administrative Fees:**
- Policy maintenance fees
- Processing fees
- Typically $50-150 annually

**Fund Management Fees:**
- Subaccount expense ratios
- Typically 0.5-2% depending on fund
- Similar to mutual fund fees

**Surrender Charges:**
