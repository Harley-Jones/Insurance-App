# 2. Key Components

## A. Premium Flexibility

**How It Works:**
- Premiums **NOT fixed** (unlike whole life)
- Variable within minimums and maximums
- Must fund enough to keep policy in force
- Overfunding allowed (within MEC rules)

**Strategic Consideration:**
High early funding is critical for long-term success

## B. Cash Value Subaccounts

**The Core Feature:**
- Funds invested in **market-based subaccounts**
- Operate like mutual funds inside the policy
- You choose allocations:
  - Aggressive (equity-heavy)
  - Conservative (bond-heavy)
  - Balanced (mixed allocation)
  - Sector-specific (technology, healthcare, etc.)

**Performance:**
- Not guaranteed
